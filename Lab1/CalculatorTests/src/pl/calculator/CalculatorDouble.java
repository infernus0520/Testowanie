package pl.calculator;

public class CalculatorDouble {
	
	public double add(double l1,double l2){
		return l1+l2;
	}
	public double sub(double l1,double l2){
		return l1-l2;
	}
	public double div(double l1,double l2){
		return l1/l2;
	}
	public double mul(double l1,double l2){
		return l1*l2;
	}
	public boolean greater(double l1,double l2){
		return(l1>l2) ? true : false;
	}
}
